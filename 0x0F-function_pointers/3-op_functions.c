#include "3-calc.h"
/**
* op_add - the sum of a and b. Prototype
* @a: num
* @b: num
* Return:results
*/

int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - difference of a and b. Prototype
* @a: num
* @b: num
* Return:results
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - returns the product of a and b.
* @a: num
* @b: num
* Return:results
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*  op_div - returns the result of the division of a by b.
* @a: num
* @b: num
* Return:results
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod -  the remainder of the division of a by b.
* @a: num
* @b: num
* Return:results
*/
int op_mod(int a, int b)
{
	return (a % b);
}
